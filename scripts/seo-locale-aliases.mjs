export function buildAliasRoutes(locales) {
  const routes = [];
  for (const [locale, map] of Object.entries(LOCALE_SEO_SLUG_ALIASES)) {
    if (!locales.includes(locale)) continue;
    for (const [alias, canonical] of Object.entries(map)) {
      const canonicalPath = canonical.includes("/") ? `/${canonical}` : `/${canonical}`;
      routes.push({
        locale,
        aliasPath: `/${alias}`,
        canonicalPath,
        publicRoute: `/${locale}/${alias}`,
      });
    }
  }
  return routes;
}
